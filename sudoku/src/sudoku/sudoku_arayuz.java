    public boolean kontrolEt(){ 
        int a;
        for(int i = 0; i < 9 ; i++){
            a = 1;
            for(int j = 0; j < 9 ; j++){
                for(int x=a ;x<9;x++){
                    if(Isayilar[i][j] == Isayilar[i][x]){
                        return false;
                    }
                }
                a++;
            }
        }return true;
    }
